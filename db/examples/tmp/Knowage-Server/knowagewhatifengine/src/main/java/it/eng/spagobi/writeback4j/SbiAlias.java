package it.eng.spagobi.writeback4j;

import java.io.Serializable;

/**
 * @author Marco Cortella (marco.cortella@eng.it)
 * 
 */
public class SbiAlias implements Serializable {

	private String name; // dimension/hierarchy name
	private String alias;// corresponding alias
	private String type;// type of alias (dimension,hierarchy...)

	/**
	 * @param name
	 *            dimension/hierarchy Name
	 * @param alias
	 *            corresponding Alias
	 * @param type
	 */
	public SbiAlias(String name, String alias, String type) {
		super();
		this.name = name;
		this.alias = alias;
		this.type = type;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the alias
	 */
	public String getAlias() {
		return alias;
	}

	/**
	 * @param alias
	 *            the alias to set
	 */
	public void setAlias(String alias) {
		this.alias = alias;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

}
